#include "libft.h"

int	ft_sqrt_rounded(int nb)
{
	int c;

	c = 0;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	while (c <= nb && c <= 46340)
	{
		if (c * c >= nb)
			return (c);
		c++;
	}
	return (0);
}
