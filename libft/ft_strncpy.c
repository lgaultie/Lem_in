#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	unsigned int count;

	count = 0;
	while (count < len && src[count] != '\0')
	{
		dst[count] = src[count];
		count++;
	}
	while (count < len)
	{
		dst[count] = '\0';
		count++;
	}
	return (dst);
}
