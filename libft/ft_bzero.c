#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char			*tab;
	unsigned int	i;

	i = 0;
	tab = (char*)s;
	if (n != 0)
	{
		while (i < n)
		{
			tab[i] = '\0';
			i++;
		}
	}
}
